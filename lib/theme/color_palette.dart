import 'package:fluent_ui/fluent_ui.dart';

class MyThemeColors {
  static Color darkBlue() {
    return const Color.fromARGB(255, 3, 4, 94);
  }

  static Color regularBlue() {
    return const Color.fromARGB(255, 0, 180, 216);
  }

  static Color lightBlue() {
    return const Color.fromARGB(255, 144, 224, 239);
  }

  static Color greyBlue() {
    return const Color.fromARGB(255, 202, 240, 248);
  }

  static Color textTitle() {
    return Colors.black;
  }

  static textSubtitle() {
    return const Color.fromARGB(255, 181, 181, 179);
  }
}
